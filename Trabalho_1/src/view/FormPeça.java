    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int cod = this.intCampoTelas(this.idTxt.getText());
        if (cod > 0) {
            Peça peça = new Peça(cod);
            PeçaDao peçaDao = new PeçaDao(ViewSistema.getConexao());
            try {
                peçaDao.delete(peça);
            } catch (SQLException ex) {
                Logger.getLogger(FormPeça.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.limpatela();
            this.atualizaLista();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void pecaTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pecaTableMouseClicked
        if (evt.getClickCount() > 1) {
            JTable obj = (JTable) evt.getComponent();
            int linha = obj.getSelectedRow();
            Integer cod = (Integer) obj.getModel().getValueAt(linha, 0);
            Peça peça = new Peça(cod);
            this.preencheTela(peça);
        }
    }//GEN-LAST:event_pecaTableMouseClicked

    private void atualizaLista() {
        try {
            PeçaDao peçaDAO = new PeçaDao(ViewSistema.getConexao());
            List<Peça> lista = peçaDAO.listaTodos();
            DefaultTableModel dtm = (DefaultTableModel) this.pecaTable.getModel();
            dtm.setRowCount(0);
            for (Peça peça : lista) {
                dtm.addRow(new Object[]{peça.getIdpeça(), peça.getNome(), peça.getValor(), peça.getNomeFornecedor()});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, String.format("Erro ao ler os Peças:%s", ex.getMessage()), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    private boolean preencheTela(Peça peça) {
        boolean ret = false;
        try {
            PeçaDao peçaDAO = new PeçaDao(ViewSistema.getConexao());
            peça = peçaDAO.retrieve(peça);
            if (peça != null && peça.getIdpeça() > 0) {
                this.nomeTxt.setText(peça.getNome());
                this.valorTxt.setText(Integer.toString(peça.getValor()));
                this.fornecedorTxt.setText(peça.getNomeFornecedor());
                this.idTxt.setText(Integer.toString(peça.getIdpeça()));
                ret = true;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, String.format("Não foi possível carregar Peça: %s", ex.getMessage()), "Erro", JOptionPane.ERROR_MESSAGE);

        }
        return ret;
    }

    private void limpatela() {
        this.idTxt.setText("");
        this.nomeTxt.setText("");
        this.valorTxt.setText("");
        this.fornecedorTxt.setText("");

    }

    private int intCampoTelas(String valor) {
        int ret = 0;
        try {
            ret = Integer.parseInt(valor);
        } catch (NumberFormatException ex) {
        }
        return ret;
    }

