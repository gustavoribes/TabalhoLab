    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            Peça peça = new Peça();
            peça.setNome(this.nomeTxt.getText());
            peça.setValor(Integer.parseInt(this.valorTxt.getText()));
            peça.setNomeFornecedor(this.fornecedorTxt.getText());

            PeçaDao peçadao = new PeçaDao(ViewSistema.getConexao());
            int cod = this.intCampoTelas(this.idTxt.getText());
            if (cod > 0) {
                peça.setIdpeça(cod);
                peçadao.update(peça);
                this.atualizaLista();
                this.limpatela();

            } else {

                peçadao.create(peça);
                this.atualizaLista();
                this.limpatela();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Número mal formatado [valor]");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

