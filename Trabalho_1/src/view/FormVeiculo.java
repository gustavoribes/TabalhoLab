    private void excluirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirButtonActionPerformed
        int cod = this.intCampoTelas(this.idTxt.getText());
        if (cod > 0) {
            Veiculo veiculoc = new Veiculo(cod);
            VeiculoDao veiculocDao = new VeiculoDao(ViewSistema.getConexao());
            try {
                veiculocDao.delete(veiculoc);
            } catch (SQLException ex) {
                Logger.getLogger(FormVeiculo.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.limpatela();
            this.atualizaLista();
        }
    }//GEN-LAST:event_excluirButtonActionPerformed

    private boolean preencheTela(Veiculo veiculo) {
        boolean ret = false;
        try {
            VeiculoDao veiculoDAO = new VeiculoDao(ViewSistema.getConexao());
            veiculo = veiculoDAO.retrieve(veiculo);
            if (veiculo != null && veiculo.getId() > 0) {
                this.fabricanteTxt.setText(veiculo.getFabricante());
                this.anoTxt.setText(Integer.toString(veiculo.getAno()));
                this.modeloTxt.setText(veiculo.getModelo());
                this.idTxt.setText(Integer.toString(veiculo.getId()));
                this.kmTxt.setText(Integer.toString(veiculo.getKm()));
                this.clienteCombo.setSelectedItem(veiculo.getCliente());
                ret = true;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, String.format("Não foi possível carregar Funcionário: %s", ex.getMessage()), "Erro", JOptionPane.ERROR_MESSAGE);

        }
        return ret;
    }

    private void atualizaLista() {
        try {
            VeiculoDao veiculoDAO = new VeiculoDao(ViewSistema.getConexao());
            List<Veiculo> lista = veiculoDAO.listaTodos();
            DefaultTableModel dtm = (DefaultTableModel) this.veiculoTable.getModel();
            dtm.setRowCount(0);
            for (Veiculo veiculo : lista) {
                dtm.addRow(new Object[]{veiculo.getId(), veiculo.getCliente().getNome(), veiculo.getModelo(), veiculo.getFabricante(), veiculo.getAno(), veiculo.getKm()});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, String.format("Erro ao ler os Veiculos:%s", ex.getMessage()), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void atualizaComboCliente() {
        try {
            ClienteDao clienteDAO = new ClienteDao(ViewSistema.getConexao());
            List<Cliente> lista = clienteDAO.listaTodos();
            Vector v = new Vector(lista);
            v.add(0, "");
            ComboBoxModel cbm = new DefaultComboBoxModel(v);
            this.clienteCombo.setModel(cbm);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, String.format("Erro ao ler os Clientes:%s", ex.getMessage()), "Erro", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(FormCliente.class.getName()).log(Level.WARNING, "Erro ao ler os Clientes", ex);
        }
    }

    private void limpatela() {
        this.anoTxt.setText("");
        this.fabricanteTxt.setText("");
        this.modeloTxt.setText("");
        this.kmTxt.setText("");
        this.idTxt.setText("");
        this.clienteCombo.setSelectedItem("");

    }

    private int intCampoTelas(String valor) {
        int ret = 0;
        try {
            ret = Integer.parseInt(valor);
        } catch (NumberFormatException ex) {
        }
        return ret;
    }
