    private void salvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarButtonActionPerformed

        try {
            Veiculo veiculo = new Veiculo();
            veiculo.setFabricante(this.fabricanteTxt.getText());
            veiculo.setModelo(this.modeloTxt.getText());
            veiculo.setAno(Integer.parseInt(this.anoTxt.getText()));
            veiculo.setKm(Integer.parseInt(this.kmTxt.getText()));
            Object obj = this.clienteCombo.getModel().getSelectedItem();
            if (obj instanceof Cliente) {
                veiculo.setCliente((Cliente) obj);
            } else {
                JOptionPane.showMessageDialog(null, "O proprietario do veiculo deve ser informado!!");
                return;
            }
            VeiculoDao veiculodao = new VeiculoDao(ViewSistema.getConexao());
            int cod = this.intCampoTelas(this.idTxt.getText());
            if (cod > 0) {
                veiculo.setId(cod);
                veiculodao.update(veiculo);
                this.atualizaLista();
                this.limpatela();

            } else {

                veiculodao.create(veiculo);
                this.atualizaLista();
                this.limpatela();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Número mal formatado [Ano | Kilometragem]");
        }
    }//GEN-LAST:event_salvarButtonActionPerformed

